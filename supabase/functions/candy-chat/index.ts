import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type",
};

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Candy AI chat request with", messages.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { 
            role: "system", 
            content: `You are Candy üç¨, a security-focused AI assistant specialized in threat detection and message analysis. Your primary functions are:

1. **Threat Detection**: Analyze messages for spam, phishing attempts, scams, malware links, social engineering attacks, and other security threats.
2. **Content Safety**: Identify toxic content, harassment, hate speech, and inappropriate messages.
3. **Privacy Protection**: Detect potential privacy violations, doxxing attempts, and sensitive information exposure.
4. **Message Analysis**: Provide tone analysis, sentiment detection, and context interpretation.
5. **Recommendations**: Offer clear, actionable security advice when threats are detected.

When analyzing messages:
- Be thorough but concise
- Use clear risk levels: SAFE, LOW RISK, MEDIUM RISK, HIGH RISK, CRITICAL
- Explain WHY something is suspicious
- Provide specific recommendations for handling threats
- Use emojis appropriately: ‚úÖ (safe), ‚ö†Ô∏è (warning), üö® (critical)

Keep responses helpful, friendly, and security-focused.`
          },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limits exceeded, please try again later." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Payment required, please add funds to your Lovable AI workspace." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      return new Response(
        JSON.stringify({ error: "AI gateway error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Candy chat error:", error);
    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
